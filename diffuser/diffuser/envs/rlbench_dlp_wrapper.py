"""
Live RLBench rollout wrapper for the 3D voxel-DLP language-conditioned policy.

Mirrors the offline preprocessing pipeline (lpwm-dev):
  RGB + depth from each cam (RLBench obs)
    -> backproject to camera coords using intrinsics
    -> transform to world via extrinsics
    -> fuse all cams into one point cloud
    -> per-frame normalize to unit cube
    -> voxelize via VoxelGridXYZ(mode='avg_rgb') => [3, D, H, W]
    -> stack with occupancy 4th channel => [4, D, H, W] RGBO
    -> DLP prior (kp, cov) on RGB-only voxels
    -> DLP full forward with precomputed prior in meta => particle tokens
    -> pack via pack_tokens_k24: [z(3), z_scale(3), z_depth(1), obj_on(1), feat(F)]

The wrapper holds a frozen DLP and runs the encoding inline at every reset/step,
so the policy sees particle tokens with the same layout as the training pkls
generated by lpwm-dev/scripts/ec_diffuser_voxel_preprocess_rlbench.py.

Action convention exposed by RLBench tasks via
  MoveArmThenGripper(EndEffectorPoseViaPlanning, Discrete) is
    [pos(3), quat(4, xyzw), gripper_open(1)]   (8D)
The diffusion policy emits actions in 10D format used during preprocessing:
    [pos(3), rot6d(6), gripper_open(1)]
so this wrapper converts rot6d -> quat at step time.
"""
import os
import re

import numpy as np
import torch


RLBENCH_TASKS = [
    "close_jar",
    "open_drawer",
    "sweep_to_dustpan_of_size",
    "meat_off_grill",
    "turn_tap",
    "slide_block_to_color_target",
    "put_item_in_drawer",
    "reach_and_drag",
    "push_buttons",
    "stack_blocks",
]

DEFAULT_CAMS = ["front", "overhead", "left_shoulder", "right_shoulder"]

# Workspace bounds used by the offline pipeline that produced the user's
# training voxel cache. Confirmed by inspecting
# data/rlbench/train_data/close_jar/.../voxel_cache/000000_meta.pt:
#   pmin = (-0.6, -0.65, 0.7),  pmax = (1.2, 0.65, 1.85)
# These are passed DIRECTLY to VoxelGridXYZ as the voxelization bounds (no
# per-frame normalization step), so live encoding lands in the exact same
# coordinate frame the DLP saw at training time. Override via the
# `workspace_bounds` constructor arg if your task uses different bounds.
DEFAULT_WORKSPACE_BOUNDS = (-0.6, -0.65, 0.7, 1.2, 0.65, 1.85)  # (xmin,ymin,zmin, xmax,ymax,zmax)


