def extract_rlbench_task_name(pkl_or_calib_path: str) -> str:
    path = pkl_or_calib_path.lower()
    for name in RLBENCH_TASKS:
        if name in path:
            return name
    m = re.search(r"rlbench[_/]([a-z_]+?)(?:_d\d+|/|\.|$)", path)
    if m:
        return m.group(1)
    raise RuntimeError(f"Cannot extract RLBench task name from: {pkl_or_calib_path}")


def _snake_to_pascal(name: str) -> str:
    return "".join(p.capitalize() for p in name.split("_"))


def _resolve_task_class(task_name: str):
    import importlib
    module = importlib.import_module(f"rlbench.tasks.{task_name}")
    return getattr(module, _snake_to_pascal(task_name))


# ---------------------------------------------------------------------------
# Rotation conversion helpers (rot6d <-> quat). Mirrors the preprocessing
# convention so live observations have the same gripper_state format as
# training pkls.
# ---------------------------------------------------------------------------

def quat_xyzw_to_rot6d(quat: np.ndarray) -> np.ndarray:
    x, y, z, w = quat[0], quat[1], quat[2], quat[3]
    r00 = 1 - 2 * (y * y + z * z)
    r10 = 2 * (x * y + w * z)
    r20 = 2 * (x * z - w * y)
    r01 = 2 * (x * y - w * z)
    r11 = 1 - 2 * (x * x + z * z)
    r21 = 2 * (y * z + w * x)
    return np.array([r00, r10, r20, r01, r11, r21], dtype=np.float32)


def rot6d_to_quat_xyzw(rot6d: np.ndarray) -> np.ndarray:
    rot6d = np.asarray(rot6d, dtype=np.float64).reshape(6)
    a1 = rot6d[:3]; a2 = rot6d[3:]
    b1 = a1 / (np.linalg.norm(a1) + 1e-12)
    b2 = a2 - np.dot(b1, a2) * b1
    b2 = b2 / (np.linalg.norm(b2) + 1e-12)
    b3 = np.cross(b1, b2)
    R = np.stack([b1, b2, b3], axis=1)
    m00, m01, m02 = R[0, 0], R[0, 1], R[0, 2]
    m10, m11, m12 = R[1, 0], R[1, 1], R[1, 2]
    m20, m21, m22 = R[2, 0], R[2, 1], R[2, 2]
    tr = m00 + m11 + m22
    if tr > 0.0:
        s = np.sqrt(tr + 1.0) * 2.0
        w = 0.25 * s; x = (m21 - m12) / s; y = (m02 - m20) / s; z = (m10 - m01) / s
    elif (m00 > m11) and (m00 > m22):
        s = np.sqrt(1.0 + m00 - m11 - m22) * 2.0
        w = (m21 - m12) / s; x = 0.25 * s; y = (m01 + m10) / s; z = (m02 + m20) / s
    elif m11 > m22:
        s = np.sqrt(1.0 + m11 - m00 - m22) * 2.0
        w = (m02 - m20) / s; x = (m01 + m10) / s; y = 0.25 * s; z = (m12 + m21) / s
    else:
        s = np.sqrt(1.0 + m22 - m00 - m11) * 2.0
        w = (m10 - m01) / s; x = (m02 + m20) / s; y = (m12 + m21) / s; z = 0.25 * s
    q = np.array([x, y, z, w], dtype=np.float64)
    q /= (np.linalg.norm(q) + 1e-12)
    return q.astype(np.float32)


def _gripper_state_from_obs(obs) -> np.ndarray:
    """[pos(3), rot6d(6), open(1)] from a raw RLBench Observation."""
    pose = np.asarray(obs.gripper_pose, dtype=np.float32)
    pos = pose[:3]
    quat = pose[3:7]
    rot6d = quat_xyzw_to_rot6d(quat)
    if obs.gripper_open is not None:
        gopen = float(obs.gripper_open)
    elif getattr(obs, "gripper_joint_positions", None) is not None:
        gopen = float(np.clip(np.mean(obs.gripper_joint_positions) / 0.04, 0.0, 1.0))
    else:
        gopen = 1.0
    return np.concatenate([pos, rot6d, np.array([gopen], dtype=np.float32)], axis=0)


# ---------------------------------------------------------------------------
# Live voxelization helpers
# ---------------------------------------------------------------------------

def _backproject_depth_rgb(depth_m: np.ndarray, rgb: np.ndarray,
                           K: np.ndarray, T_cam2world: np.ndarray,
                           depth_max: float = 10.0) -> np.ndarray:
    """Backproject single-camera (depth in meters) + RGB into world-frame
    points + colors.

    Returns (N, 6) array: xyz (world) + rgb in [0, 1].
    """
    H, W = depth_m.shape
    Z = depth_m
    valid = np.isfinite(Z) & (Z > 1e-6) & (Z < depth_max)
    if not valid.any():
        return np.zeros((0, 6), dtype=np.float32)
    vv, uu = np.meshgrid(np.arange(H, dtype=np.float32),
                         np.arange(W, dtype=np.float32),
                         indexing="ij")
    fx = float(K[0, 0]); fy = float(K[1, 1])
    cx = float(K[0, 2]); cy = float(K[1, 2])
    Zv = Z[valid]
    Uv = uu[valid]; Vv = vv[valid]
    X = (Uv - cx) / fx * Zv
    Y = (Vv - cy) / fy * Zv
    pts_cam = np.stack([X, Y, Zv], axis=-1).astype(np.float32)
    ones = np.ones((pts_cam.shape[0], 1), dtype=np.float32)
    pts_h = np.concatenate([pts_cam, ones], axis=1)
    pts_world = (T_cam2world.astype(np.float32) @ pts_h.T).T[:, :3]
    cols = rgb[valid].astype(np.float32)
    if cols.max() > 1.5:           # uint8 PNG -> [0, 1]
        cols = cols / 255.0
    return np.concatenate([pts_world, cols], axis=-1)


def _fuse_pointcloud(rlbench_obs, cams) -> np.ndarray:
    """Walk every enabled camera on `rlbench_obs`, backproject depth+RGB to
    world coords, concatenate. Returns (N, 6) in world frame."""
    point_clouds = []
    for cam in cams:
        depth = getattr(rlbench_obs, f"{cam}_depth", None)
        rgb = getattr(rlbench_obs, f"{cam}_rgb", None)
        if depth is None or rgb is None:
            continue
        misc = getattr(rlbench_obs, "misc", {}) or {}
        K = misc.get(f"{cam}_camera_intrinsics")
        T = misc.get(f"{cam}_camera_extrinsics")
        if K is None or T is None:
            raise RuntimeError(
                f"obs.misc missing camera params for '{cam}' "
                f"(need {cam}_camera_intrinsics and {cam}_camera_extrinsics). "
                f"Available misc keys: {list(misc.keys())}"
            )
        K = np.asarray(K, dtype=np.float32)
        T = np.asarray(T, dtype=np.float32)
        depth = np.asarray(depth, dtype=np.float32)
        rgb = np.asarray(rgb)
        pc = _backproject_depth_rgb(depth, rgb, K, T)
        if pc.shape[0] > 0:
            point_clouds.append(pc)
    if not point_clouds:
        return np.zeros((0, 6), dtype=np.float32)
    return np.concatenate(point_clouds, axis=0)


def _crop_to_workspace(pts: np.ndarray, bounds) -> np.ndarray:
    """Filter (N, 6) points to an axis-aligned world-frame box.
    bounds = (xmin, ymin, zmin, xmax, ymax, zmax). Returns (M, 6) with M <= N."""
    if bounds is None or pts.shape[0] == 0:
        return pts
    xmin, ymin, zmin, xmax, ymax, zmax = bounds
    xyz = pts[:, :3]
    mask = (
        (xyz[:, 0] >= xmin) & (xyz[:, 0] <= xmax) &
        (xyz[:, 1] >= ymin) & (xyz[:, 1] <= ymax) &
        (xyz[:, 2] >= zmin) & (xyz[:, 2] <= zmax)
    )
    return pts[mask]


def _normalize_unit_cube(pts: np.ndarray) -> np.ndarray:
    """Center on bbox midpoint and ISOTROPICALLY scale xyz to fit in [-1, 1]^3.
    Mirrors lpwm-dev/scripts/preprocess_rlbench_voxels.py:center_scale_unit_cube
    so live-fused point clouds land in the same coordinate frame the DLP saw
    during training. Anisotropic per-axis stretch would warp the scene and
    squash the relevant table region into a fragment of the cube."""
    if pts.shape[0] == 0:
        return pts
    xyz = pts[:, :3]
    mins = xyz.min(axis=0)
    maxs = xyz.max(axis=0)
    center = (mins + maxs) / 2.0
    scale = float((maxs - mins).max()) + 1e-8
    out = pts.copy()
    out[:, :3] = (xyz - center[None, :]) / scale * 2.0
    return out


def _voxelize_to_rgbo(pts: np.ndarray, grid_whd, bounds=None, max_points=None) -> torch.Tensor:
    """Voxelize fused (N,6) point cloud into [4, D, H, W] RGBO tensor.

    Channels 0-2 are per-voxel average RGB (in [0, 1]).
    Channel 3 is occupancy (1 if any point fell in the voxel, else 0).

    bounds = ((xmin,ymin,zmin), (xmax,ymax,zmax)) in world frame. Passing fixed
    bounds matches the offline preprocessing meta exactly, so voxel indices
    correspond to the same world locations across frames (and to the DLP's
    training-time coordinate frame).
    """
    # Use the wrapper version (datasets/voxelize_ds_wrapper) — the
    # modules/voxel_modules version has a known bug in the avg_rgb accumulator
    # indexing. preprocess_rlbench_voxels.py uses this same source.
    from datasets.voxelize_ds_wrapper import VoxelGridXYZ
    if max_points is not None and pts.shape[0] > int(max_points):
        idx = np.random.choice(pts.shape[0], int(max_points), replace=False)
        pts = pts[idx]
    if pts.shape[0] == 0:
        D, H, W = int(grid_whd[2]), int(grid_whd[1]), int(grid_whd[0])
        return torch.zeros(4, D, H, W, dtype=torch.float32)
    pts_t = torch.from_numpy(pts[:, :3]).contiguous()
    cols_t = torch.from_numpy(pts[:, 3:6]).contiguous()
    vg = VoxelGridXYZ(pts_t, cols_t, grid_whd=tuple(grid_whd),
                      bounds=bounds, mode="avg_rgb")
    rgb_grid = vg.to_dense()                       # [3, D, H, W]
    # Occupancy: any non-zero RGB means the voxel was hit at least once.
    occ = (rgb_grid.abs().sum(dim=0, keepdim=True) > 0).float()
    return torch.cat([rgb_grid, occ], dim=0)        # [4, D, H, W]


def _pack_tokens_k24(out):
    """Pack DLP encoder output to particle tokens matching the offline pkl
    layout: fg = [z(3), z_scale(3), z_depth(1), obj_on(1), feat(F)], bg separate."""
    z       = out["z"][:, 0]
    z_scale = out["z_scale"][:, 0]
    z_depth = out["z_depth"][:, 0]
    obj_on  = out["obj_on"][:, 0]
    z_feat  = out["z_features"][:, 0]
    z_bg    = out["z_bg_features"][:, 0]
    if obj_on.dim() == 2:
        obj_on = obj_on.unsqueeze(-1)
    if z_depth.shape[-1] == 3:
        z_depth = z_depth[..., :1]
    toks = torch.cat([z, z_scale, z_depth, obj_on, z_feat], dim=-1)
    return toks, z_bg


