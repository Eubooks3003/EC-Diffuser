class RLBenchDLPEnv:
    """
    Live RLBench rollout environment for the 3D voxel-DLP language-conditioned policy.

    Construction:
        task_name:        snake_case RLBench task (e.g. "close_jar").
        dlp_model:        the loaded voxel DLP model (lpwm-dev voxel_models.DLP),
                          eval-mode and on `device`.
        dlp_cfg:          dict from DLP's hparams.json. Used to read voxel grid
                          size and channel count.
        cams:             list of camera names enabled in the live env. Default
                          = front+overhead+left_shoulder+right_shoulder, matching
                          the offline preprocessing pipeline.
        image_size:       int, square image size for all enabled cameras.
        grid_whd:         voxel grid (W, H, D). Falls back to dlp_cfg['voxel_grid_whd']
                          if available, else (64, 64, 64).
        max_fuse_points:  cap on total points fused across cams before voxelization
                          (None = no cap).
        normalize_to_unit_cube:
                          per-frame [-1,1] normalization of the fused point cloud
                          before voxelization. Matches preprocess_rlbench_voxels.py.
        device:           device for DLP inference; voxels move here automatically.
    """

    def __init__(self, task_name: str, dlp_model, dlp_cfg=None,
                 cams=None, image_size: int = 128, headless: bool = True,
                 episode_length: int = 400, action_mode_kwargs=None,
                 delta_actions: bool = False, dataset_root: str = None,
                 grid_whd=None, max_fuse_points: int = 200000,
                 normalize_to_unit_cube: bool = True,
                 workspace_bounds=DEFAULT_WORKSPACE_BOUNDS,
                 device: str = "cuda:0"):
        self.task_name = task_name
        self.dlp_model = dlp_model
        self.dlp_cfg = dlp_cfg or {}
        self.cams = list(cams) if cams is not None else list(DEFAULT_CAMS)
        self.image_size = int(image_size)
        self.headless = headless
        self.episode_length = int(episode_length)
        self.action_mode_kwargs = action_mode_kwargs or {}
        self.delta_actions = bool(delta_actions)
        self.dataset_root = dataset_root or os.environ.get("ECDIFF_DEMO_ROOT")

        # Voxel grid: prefer explicit arg, fall back to DLP cfg, finally (64,64,64).
        if grid_whd is None:
            cfg_grid = self.dlp_cfg.get("voxel_grid_whd") if self.dlp_cfg else None
            grid_whd = tuple(cfg_grid) if cfg_grid is not None else (64, 64, 64)
        self.grid_whd = tuple(int(x) for x in grid_whd)
        self.max_fuse_points = max_fuse_points
        self.normalize_to_unit_cube = bool(normalize_to_unit_cube)
        self.workspace_bounds = tuple(workspace_bounds) if workspace_bounds is not None else None
        self.device = torch.device(device)

        # Channel count the DLP expects (drop occupancy if model is RGB-only).
        # Default to 3 (the kmeans script + offline encoding both use vox[:3]).
        self._dlp_in_channels = int(self.dlp_cfg.get("ch", 3)) if self.dlp_cfg else 3

        self._env = None
        self._task = None
        self._task_class = None
        self._current_lang = None
        self._current_variation = None
        self._step_count = 0
        self._recorded_frames = []
        self._record_enabled = True
        # Most recent live encoding outputs, exposed for debug/recording.
        self._last_voxel = None
        self._last_kp = None
        self._last_cov = None
        self._last_tokens = None

    # ------------------------------------------------------------------ launch
    def _launch(self):
        from rlbench import ObservationConfig, CameraConfig
        from rlbench.action_modes.action_mode import MoveArmThenGripper
        from rlbench.action_modes.arm_action_modes import EndEffectorPoseViaPlanning
        from rlbench.action_modes.gripper_action_modes import Discrete
        from rlbench.environment import Environment
        from pyrep.const import RenderMode

        obs_config = ObservationConfig()
        obs_config.set_all(False)
        # Low-dim state needed by the policy and by gripper_state extraction.
        obs_config.gripper_pose = True
        obs_config.gripper_open = True
        obs_config.gripper_joint_positions = True
        obs_config.joint_positions = True

        cam_attrs = {
            "front": "front_camera",
            "overhead": "overhead_camera",
            "left_shoulder": "left_shoulder_camera",
            "right_shoulder": "right_shoulder_camera",
            "wrist": "wrist_camera",
        }
        for cam in self.cams:
            attr = cam_attrs.get(cam)
            if attr is None:
                raise ValueError(f"Unknown RLBench camera name: {cam}")
            # Both RGB and depth are needed for the live voxelization. Depth in
            # meters keeps the backprojection trivially correct (no scale tweak).
            cc = CameraConfig(
                image_size=(self.image_size, self.image_size),
                rgb=True, depth=True, point_cloud=False, mask=False,
                render_mode=RenderMode.OPENGL,
            )
            cc.depth_in_meters = True
            setattr(obs_config, attr, cc)

        action_mode = MoveArmThenGripper(
            arm_action_mode=EndEffectorPoseViaPlanning(**self.action_mode_kwargs),
            gripper_action_mode=Discrete(),
        )

        env_kwargs = dict(action_mode=action_mode, obs_config=obs_config,
                          headless=self.headless)
        if self.dataset_root:
            env_kwargs["dataset_root"] = self.dataset_root
        self._env = Environment(**env_kwargs)
        self._env.launch()
        self._task_class = _resolve_task_class(self.task_name)
        self._task = self._env.get_task(self._task_class)

    def shutdown(self):
        if self._env is not None:
            try:
                self._env.shutdown()
            finally:
                self._env = None
                self._task = None

    # --------------------------------------------------- live voxel encoding
    def voxelize(self, rlbench_obs) -> torch.Tensor:
        """Public helper: fuse cams + voxelize a single RLBench Observation.
        Returns a [4, D, H, W] CPU tensor (RGBO).

        Pipeline matches the offline lpwm-dev pipeline that produced the
        training voxel cache:
          fuse cams (world frame) -> crop to workspace bounds
          -> voxelize with FIXED workspace bounds (no per-frame normalize).
        Voxel indices then correspond to the same world locations across all
        frames, exactly like the offline cache and what the DLP saw at training.
        """
        pts = _fuse_pointcloud(rlbench_obs, self.cams)
        pts = _crop_to_workspace(pts, self.workspace_bounds)
        # Build fixed bounds tuple for VoxelGridXYZ (None -> per-frame fit fallback).
        vg_bounds = None
        if self.workspace_bounds is not None:
            xmin, ymin, zmin, xmax, ymax, zmax = self.workspace_bounds
            vg_bounds = ((xmin, ymin, zmin), (xmax, ymax, zmax))
        return _voxelize_to_rgbo(pts, self.grid_whd, bounds=vg_bounds,
                                  max_points=self.max_fuse_points)

    @torch.no_grad()
    def encode(self, vox: torch.Tensor):
        """Public helper: run DLP prior + full forward on a [4, D, H, W] voxel.
        Returns dict with keys 'tokens' (K, Dtok), 'bg' (bg_dim,), 'kp' (K, 3),
        'cov' (K, 3, 3)."""
        if vox.dim() == 4:
            vox = vox.unsqueeze(0)        # [1, 4, D, H, W]
        vox = vox.to(self.device)
        rgb_only = vox[:, : self._dlp_in_channels].contiguous()
        kp, cov = self.dlp_model.prior_module.encode_prior(rgb_only)
        meta = {"kmeans_kp": kp, "kmeans_cov": cov}
        out = self.dlp_model(rgb_only, deterministic=True, warmup=False,
                             with_loss=False, meta=meta)
        toks, bg_feats = _pack_tokens_k24(out)
        return {
            "tokens": toks[0].detach().cpu().numpy().astype(np.float32),
            "bg":     bg_feats[0].detach().cpu().numpy().astype(np.float32).reshape(-1),
            "kp":     kp[0].detach().cpu(),
            "cov":    cov[0].detach().cpu(),
        }

    def _make_obs_dict(self, rlbench_obs):
        vox = self.voxelize(rlbench_obs)            # [4, D, H, W] cpu
        enc = self.encode(vox)
        # Stash for debug / recording.
        self._last_voxel = vox
        self._last_kp = enc["kp"]
        self._last_cov = enc["cov"]
        self._last_tokens = enc["tokens"]

        gripper_state = _gripper_state_from_obs(rlbench_obs)         # (10,)
        self._last_gripper_pose = np.asarray(rlbench_obs.gripper_pose, dtype=np.float32)
        return {
            "obs":              enc["tokens"],
            "bg_features":      enc["bg"],
            "gripper_state":    gripper_state,
            "language":         self._current_lang,
            "variation_number": self._current_variation,
        }

    # ------------------------------------------------------------------ reset
    def _record(self, rlbench_obs):
        if not self._record_enabled:
            return
        frame = getattr(rlbench_obs, "front_rgb", None)
        if frame is None:
            return
        arr = np.asarray(frame)
        if arr.ndim == 4 and arr.shape[0] == 1:
            arr = arr[0]
        if arr.ndim == 3 and arr.shape[0] in (1, 3, 4) and arr.shape[-1] not in (1, 3, 4):
            arr = np.transpose(arr, (1, 2, 0))
        if arr.dtype != np.uint8:
            if arr.max() <= 1.5:
                arr = (np.clip(arr, 0, 1) * 255).astype(np.uint8)
            else:
                arr = np.clip(arr, 0, 255).astype(np.uint8)
        self._recorded_frames.append(arr)

    def pop_recorded_frames(self):
        frames = self._recorded_frames
        self._recorded_frames = []
        return frames

    def reset(self, variation: int = None, demo=None):
        if self._env is None:
            self._launch()
        if demo is not None:
            demo_var = int(getattr(demo, "variation_number", variation or 0))
            demo_var = demo_var % self._task.variation_count()
            self._task.set_variation(demo_var)
            self._current_variation = demo_var
            descriptions, rlbench_obs = self._task.reset_to_demo(demo)
        else:
            if variation is None:
                variation = np.random.randint(self._task.variation_count())
            variation = int(variation) % self._task.variation_count()
            self._task.set_variation(variation)
            self._current_variation = variation
            descriptions, rlbench_obs = self._task.reset()
        self._current_lang = descriptions[0] if len(descriptions) > 0 else ""
        self._step_count = 0
        self._recorded_frames = []
        self._record(rlbench_obs)
        return self._make_obs_dict(rlbench_obs)

    # ------------------------------------------------------------------ step
    def _action_to_rlbench(self, action: np.ndarray, current_pose: np.ndarray = None) -> np.ndarray:
        a = np.asarray(action, dtype=np.float32).reshape(-1)
        if a.shape[0] != 10:
            raise ValueError(f"Expected 10D action [pos(3)+rot6d(6)+open(1)], got shape {a.shape}")
        if self.delta_actions:
            if current_pose is None:
                raise RuntimeError("delta_actions=True requires current_pose")
            cur = np.asarray(current_pose, dtype=np.float32).reshape(-1)
            # Rebuild absolute target via R_target = R_cur @ R_delta, pos += delta.
            from numpy import cross
            quat_cur = cur[3:7]
            x, y, z, w = quat_cur[0], quat_cur[1], quat_cur[2], quat_cur[3]
            R_cur = np.array([
                [1 - 2 * (y * y + z * z), 2 * (x * y - w * z), 2 * (x * z + w * y)],
                [2 * (x * y + w * z), 1 - 2 * (x * x + z * z), 2 * (y * z - w * x)],
                [2 * (x * z - w * y), 2 * (y * z + w * x), 1 - 2 * (x * x + y * y)],
            ], dtype=np.float64)
            r6 = a[3:9].astype(np.float64)
            a1 = r6[:3]; a2 = r6[3:]
            b1 = a1 / (np.linalg.norm(a1) + 1e-12)
            b2 = a2 - np.dot(b1, a2) * b1
            b2 = b2 / (np.linalg.norm(b2) + 1e-12)
            b3 = cross(b1, b2)
            R_delta = np.stack([b1, b2, b3], axis=1)
            R_target = R_cur @ R_delta
            # matrix -> quat (Shepperd)
            m00, m01, m02 = R_target[0, 0], R_target[0, 1], R_target[0, 2]
            m10, m11, m12 = R_target[1, 0], R_target[1, 1], R_target[1, 2]
            m20, m21, m22 = R_target[2, 0], R_target[2, 1], R_target[2, 2]
            tr = m00 + m11 + m22
            if tr > 0.0:
                s = np.sqrt(tr + 1.0) * 2.0
                qw = 0.25 * s; qx = (m21 - m12) / s; qy = (m02 - m20) / s; qz = (m10 - m01) / s
            elif (m00 > m11) and (m00 > m22):
                s = np.sqrt(1.0 + m00 - m11 - m22) * 2.0
                qw = (m21 - m12) / s; qx = 0.25 * s; qy = (m01 + m10) / s; qz = (m02 + m20) / s
            elif m11 > m22:
                s = np.sqrt(1.0 + m11 - m00 - m22) * 2.0
                qw = (m02 - m20) / s; qx = (m01 + m10) / s; qy = 0.25 * s; qz = (m12 + m21) / s
            else:
                s = np.sqrt(1.0 + m22 - m00 - m11) * 2.0
                qw = (m10 - m01) / s; qx = (m02 + m20) / s; qy = (m12 + m21) / s; qz = 0.25 * s
            quat = np.array([qx, qy, qz, qw], dtype=np.float64)
            quat = quat / (np.linalg.norm(quat) + 1e-12)
            quat = quat.astype(np.float32)
            pos = (cur[:3] + a[:3]).astype(np.float32)
        else:
            pos = a[:3].copy().astype(np.float32)
            quat = rot6d_to_quat_xyzw(a[3:9])
        gopen = np.array([1.0 if a[9] >= 0.5 else 0.0], dtype=np.float32)
        ignore_coll = np.array([1.0], dtype=np.float32)
        return np.concatenate([pos, quat, gopen, ignore_coll], axis=0)

    def step(self, action):
        from rlbench.backend.exceptions import InvalidActionError
        try:
            from pyrep.errors import IKError, ConfigurationPathError
            _path_errs = (IKError, ConfigurationPathError, InvalidActionError)
        except Exception:
            _path_errs = (InvalidActionError,)

        rlb_action = self._action_to_rlbench(
            action, current_pose=getattr(self, "_last_gripper_pose", None)
        )
        info = {"variation_number": self._current_variation,
                "language": self._current_lang}
        MAX_RETRIES = 10
        rlbench_obs, reward, terminal = None, 0.0, False
        caught = []
        for _ in range(MAX_RETRIES):
            try:
                rlbench_obs, reward, terminal = self._task.step(rlb_action)
                break
            except _path_errs as e:
                caught.append(type(e).__name__)
                continue
        if rlbench_obs is None:
            info["error"] = caught[-1] if caught else "PlanningError"
            info["retry_history"] = caught
            return None, 0.0, True, info
        if caught:
            info["retried"] = caught

        # Discrete-gripper release workaround (matches rlbench_2d wrapper).
        if rlb_action[7] >= 0.5:
            try:
                gripper = self._task._task.robot.gripper
                if len(gripper.get_grasped_objects()) > 0:
                    gripper.release()
            except Exception:
                pass

        self._step_count += 1
        if self._step_count >= self.episode_length:
            terminal = True
        self._record(rlbench_obs)
        return self._make_obs_dict(rlbench_obs), float(reward), bool(terminal), info

    @property
    def language(self) -> str:
        return self._current_lang

    @property
    def variation_number(self) -> int:
        return self._current_variation
